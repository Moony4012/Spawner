using System.Collections;
using System.Collections.Generic;
using UnityEngine;

   public struct StatCharacter
    {
        public string name;
        public int vie;
        public int defense;
        public int attack;
    public StatCharacter(string name, int vie, int defense, int attack)
    {
        this.name = name;
        this.vie = vie;
        this.defense = defense;
        this.attack = attack;
    }
}
   

